        <div class="mt-20" style="padding: 10px; background-color:#FF5722; color:whitesmoke; font-weight:bold; margin-bottom:10px">
            1. Communities have capacity to secure their rights and entitlements through interaction with service providers and accountable institutions.
        </div>
        <p class="text-justify mt-10">


Social mobilization and empowerment is an integral part of the interventions: Empowerment of the people is at the heart of the BDS program and people are made aware of their rights. Community capacity building is included in program design, implementation and monitoring stages.

Advocacy is taken on board as necessary for sustainability and long term structural and policy change. BDS will not only continue filling in gaps in public services to the targeted communities, rather also contribute to strengthening those services in one hand and on other hand supporting the communities to claim for their right and benefit from public services.

</p>
<div class="mt-20" style="padding: 10px; background-color:#FF5722; color:whitesmoke; font-weight:bold; margin-bottom:10px">
    2. Communities enjoy healthy lives in clean environments with access to safe drinking water, shelter & sanitation facilities.
</div>

<p class="text-justify mt-10">
    Drinking water availability in Sri Lanka is significantly challenged due to changing climatic patterns. All districts were reported as having more than 50% of the households covered with safe drinking water. However, there are district disparities. Nuwara Eliya (54%) district has relatively low access to safe drinking water sources and it will receive prioritized attention in future interventions.

    The quality of safe drinking water is also an issue in certain parts of the country. The availability of hard metals and agrochemical contaminations have largely caused water to become unsuitable for drinking in agricultural areas in the North Central and Northern Province. As a result, the prevalence of the Chronic Kidney Disease of Unknown etiology has become a notable issue.

    As far as sanitation facilities are concerned, 87% of the population possess onsite sanitation facilities. However, about 4.4 percent of households are not using a toilet at all.

    <br>
</p>
<p class="text-justify mt-10">
    The situation of wastewater disposal is also dismal. Only 2% households currently have piped sewerage connections. The rest of the households disposes wastewater using onsite pits within the premises which could lead to various environmental and health issues, especially contamination of groundwater.

    BDS has collaborated with the Plantation sector agencies to support construction of houses in the plantation sector. Only 15.8% families are provided with an individual house in 7 perch land with essential utilities which is the expected model.

</p>

<div class="mt-20" style="padding: 10px; background-color:#FF5722; color:whitesmoke; font-weight:bold; margin-bottom:10px">
    3. Secured income and food security of people through sustainable livelihood
</div>

<p class="text-justify mt-10">
    Berendina Development Services (BDS), since its inception in 1987, has engaged in economic development of poor communities in Sri Lanka.
    <br><br>
    It is imperative that sustainable livelihood is key to overcome poverty that is persistent among marginalized communities. In order to overcome this poverty, it needs to be understood through a cause and effect analysis of the poverty stricken population in a community. In detail we explore who are the people who are poor, why they have become poor and excluded and gendered analysis of poverty. We find out in detail the causes of poverty, and the effect of it. Thus, we apply a holistic approach in livelihood development. Profitable and resilient livelihoods are promoted among the poor by developing human capital, natural capital, social capital, financial capital and physical capital of them.
</p>


<div class="mt-20" style="padding: 10px; background-color:#FF5722; color:whitesmoke; font-weight:bold; margin-bottom:10px">
    4. Up skilled, innovative and well-connected youth are productively engaged and lead communities towards prosperous and resilient future.
</div>

<p class="text-justify mt-10">
    The country’s youth, who account for almost one fourth of the total population, will be engaged as proactive partners in the country’s sustainable development process. We will identify their aspirations and develop an effective programme to enable young people to reach their full potential. Our aim is to be innovative in increasing the employability of the marginalized youth to match the skill requirements of the local and foreign labor markets.

    BDS guides the communities served to utilize capacities and skills of youth for fulfilling common/community needs as well as their personal development needs. Those needs can vary from forming their own income generation activities, provided they have the capacity and aptitude for such self-employment, or to be employed, or to play roles in community leadership to contribute actively to community development. Given the existing gender-based inequalities, also among young people, we also make efforts to address the stereotypical patriarchal culture into a more effective, progressive and transformative environment, working actively towards gender justice. Thus, focusing on the overall fulfilment of young people’s needs to enjoy successful, safe and gender just young lives.
</p>

