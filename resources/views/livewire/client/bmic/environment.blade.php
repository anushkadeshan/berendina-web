    <h5 class="line-bottom mt-20"><b>Waste Paper Recycling Program to reduce Ecological Foot Print on Paper Usage</b>
    </h5>
    <p class="text-justify mt-20" style="text-align: justify;">
        it is a notable fact that most companies use paper for all documentation purposes which can lead to adverse
        impacts if not managed in a sustainable manner. Understanding this fact, BMIC promotes “paper to paper” concept
        instead of “tree to paper” concept by launching a wastepaper recycling program at Head Office in collaboration
        with Neptune Recyclers (Pvt) Limited a local recycling company to reduce the ecological foot print of paper
        usage at the working environment. Staffs are advised to minimize the use of paper and encouraged for electronic
        documentation methodology.
