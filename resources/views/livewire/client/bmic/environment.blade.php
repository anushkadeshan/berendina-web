    profitability for families by reducing fuel and electricity cost in household. In addition, slurry generated
    through biogas digester is a good organic fertilizer which can be used for not only on home gardening but for
    the commercial agriculture activities as well. The project facilitated such market transformation of Renewable
    Energy technologies and other co-benefits by introducing bio gas digesters to clients.
