    <h4 class="line-bottom mt-20">BMIC’s Environmental approach</h4>
    <p class="text-justify mt-20" style="text-align: justify;">BMIC follows the triple bottom line approach of
        microfinance - people, planet and profit. BMIC has a dedicated approach under E type products of enterprise
        development services to ensure minimising possible environmental damages caused by the business activities of
        its clients. This is done by:
    </p>
    <ul class="list-icon theme-colored" style="font-size: 4px;">
        <li><i class="fa fa-arrow-right"></i>Identification of enterprises which create adverse impact to natural
            environment and extending supports to implement protection measures</li>
        <li><i class="fa fa-arrow-right"></i>Identification of improving greener aspects of existing enterprise by using
            its waste as resources and create economic value in it</li>
        <li><i class="fa fa-arrow-right"></i>Identification of potential greener businesses and facilitate for the
            implementation of the same.</li>
        <li><i class="fa fa-arrow-right"></i>Creates awareness among clients and general public to conserve environment
        </li>
        <li><i class="fa fa-arrow-right"></i>Implements occupational health and safety program to mitigate adverse
            health impacts of business with the support of government, private institutes and Non-Government
            Organizations to secure the long term sustainability of enterprises. </li>
        <li><i class="fa fa-arrow-right"></i>To increase the marketing opportunities </li>
        <li><i class="fa fa-arrow-right"></i>Promoting Eco friendly office practices </li>
    </ul>

