    <h5 class="line-bottom mt-20"><b>Health & Safety Program </b></h5>
    <p class="text-justify mt-20" style="text-align: justify;">
        BMIC BMIC identifies enterprises which may harm to the people due to non-adherence of proper occupational health
        and safety measures.

        BMIC supports to provide comprehensive solutions to such issues by providing safety gears as well as conducting
        awareness sessions.
        
        One of the traditional industries of Sri Lanka with a long history is making iron and wrought iron tools. Many
        rural villagers engage in this industry as their main livelihood activity. People in Kottalbadda in Anuradhapura
        are famed for their specialized skills in iron and metal works and have been engaged in this industry for many
        generations.
        their specialized skills in iron and metal works and have been engaged in this industry for many generations.
        <br><br>


        Due to the high prices in purchasing protective gears and no proper knowledge on how to use them every day, most
        of the blacksmiths ignore wearing them and take a great deal of risk in their day-to-day work environment. This
        attitude creates safety issues as well as health complications in a longer run.
        <br><br>
        To mitigate this prolonged issue, Berendina organized a Health and Safety Awareness Program at Ipalogama Branch
        for 15 micro credit clients attached to the BMIC branch who are into iron work industry. The objective of the
        session was to enhance their knowledge and importance of taking safety precautions and wearing safety gears.
